import mongoose from 'mongoose';


const deliveryEmailSchema = new mongoose.Schema(
    {
        name: {
            type: String,
            required: true
        },
        email: {
            type: String,
            required: true
        },
        createdAt: {
            type: Date,
            default: Date.now
        },
        updatedAt: {
            type: Date,
            default: Date.now
        }
    },
    {
        versionKey: false
    }
)


export default mongoose.model('DeliveryEmail', deliveryEmailSchema);
